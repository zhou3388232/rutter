    <p>
      Derivatives vary in complexity from fairly simple “plain vanilla”
      transactions to highly exotic structures. In order to make a sound
      decision about a potential derivative transaction, an end-user (whether
      hedger or speculator) needs to understand fully its risk/return profile
      at inception and throughout its life, and this can be particularly difficult
      for small to mid-sized corporate and retail market participants
      considering “complex” structures. Many of these market participants
      will want to seek out independent advice.
    </p>
    <p>In fact, European financial markets will soon be subject to the Markets
      in Financial Instruments Directive (or “MiFid II”) that requires dealers
      to conduct appropriateness testing for products classified as “complex”
      in order to determine suitability for an end-user. MiFid II has generated
      much discussion about the definition of “complex” in the context of
      financial derivatives, and much concern is expressed about the
      seemingly arbitrary nature of any cut-off between “simple” and
      “complex”. Rutter Associates sees discussions about regulatory “cutoffs”
      as ultimately unimportant to end-users – what is “complex” to one
      end-user may be relatively “simple” to another. But how might a
      particular end-user gauge “complexity”?
    </p>
     <p>
      “Complexity” relates to the costs incurred in modeling effort and data
      acquisition required to perform an adequate analysis of ex-ante
      derivative risk/return profiles and valuation and risk management
      throughout the derivative’s life. “Complexity” is not the same as “risk”.
      The mere fact that a derivative is complex does not mean that it is
      inappropriately risky, but it does mean that far more resources must be
      expended to understand its risk/return profile and its effectiveness in a
      hedge application than would otherwise be expended on analyzing
      simpler structures. And to be sure, there are economies of scale in
      analyzing derivatives – fixed costs fall with increasing volumes of trades 
      being examined and variable costs fall as operational efficiency
      improves with volume. These analysis costs cannot be avoided by endusers
      without incurring potentially greater costs of heightened
      operational risk (i.e., failures of people and processes in risk
      management) and legal risk of costly litigation with uncertain outcomes.
      Therefore, it is advisable for an end-user to consider these analysis costs
      (those at inception and over the life of the trade) as part of the cost of
      transacting a complex derivative and to take these costs into account in
      deciding between a complex derivative and a simpler alternative.
    </p>
    <p>
      The questions Rutter Associates advises potential derivative end-users
      (hedgers and speculators) to ask their dealers and/or independent
      advisors before transacting a derivative contract include the following:
    </p>
    <ol>
      <li>If a hedge transaction, do the derivatives actually match closelythe risk exposures we are trying to hedge? 
        <ol>
          <li>Do they present a sufficient level of appropriately definedhedge effectiveness?</li>
          <li>Are there scenarios where the derivative can create a net
              loss to the combined position although the underlying
              account would have registered a gain? If so, would these
              scenarios cause the end-user financial distress?</li>
          <li>Are there scenarios where the derivative can reinforce a
              loss to the underlying account, i.e., where the combined
              position loses more than would have the unhedged
              underlying account? If so, would these scenarios cause the
              end-user financial distress?</li>
        </ol>
      </li>
      <li>Do the derivatives embed “knock out” barriers limiting upside potential?</li>
      <li>Is downside potential limited or is there no effective practical limit to potential liability?</li>
      <li>Are the derivatives “geared” (i.e., levered) in such a manner
          that a given change in the underlying generating a positive
          impact on the derivative price generates a much larger
          negative impact if the change in underlying is of opposite sign
          (for example in the case of an interest rate derivative, are 
          potential gains and losses asymmetric such that the losses
          from a 100 basis point increase in rates are twice or three
          times the amount of the gains from a 100 basis point
          decrease?)
      </li>
      <li>What embedded options are we selling to the dealer in the
      transaction? (“Zero cash outlay” does not mean “zero cost” www.rutterassociates.com
      when transacting a derivative; if the end-user is being sold an
      option profile and no cash is charged, it is likely that the enduser
      is implicitly writing an option).
      </li>
      <li>How will we monitor the fair market price and evolving risk
      profile of the derivative throughout its lifetime? Do we have
      access to the models required for valuation, scenario analysis
      and stress testing?
      </li>
    </ol>
    <p>In order to help retail and corporate decision makers gain a sense of
    whether a proposed derivative transaction is “too complex” to enter
    into without the application of additional resources, Rutter Associates
    has created a “complexity calculator”, that assesses “complexity” with
    respect to the access to information and analytical sophistication of the
    end-user. A series of simple questions about the derivative under
    consideration is posed, and a level of relative complexity is returned.
    </p>
    <p>The following is a sample of features about which the calculator asks:</p>
    <ol>
      <li>The existence of early termination options and triggers;</li>
      <li>Limits on downside risk;</li>
      <li>“Gearing” or “leverage”;</li>
      <li>Types of and number of instruments underlying the derivative;</li>
      <li>Nature of the payoff formulation;</li>
      <li>Availability of market data for derivative pricing;</li>
      <li>Availability of appropriate models for fair value determination
      including valuation adjustments for credit and funding;</li>
      <li>Potential hedge effectiveness.</li>
    </ol>
